    public boolean ingresar() {

        String linea = "";
        String valores[];
        String datos = " ";
        File archivo = new File("C:\\Users\\usuario\\Documents\\ejemplologin.csv");
        FileReader fr;

        try {
            fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);

            while (br.ready()) {

                if (!(linea = br.readLine()).equals(" ")) {
                   
                    valores = linea.split(",");
                    String usuario = valores[0].toString();
                    String password = valores[1].toString();
                    if (usuario.equals(txt_usuario.getText())) {
                        if (password.equals(txt_contrasenia.getText())) {
                            JOptionPane.showMessageDialog(null, " Datos correctos");
                            txt_usuario.setText("");
                            txt_contrasenia.setText("");

                            return true;

                        } else {
                            JOptionPane.showMessageDialog(null, " Datos erroneos");
                            txt_usuario.setText("");
                            txt_contrasenia.setText("");
                            return false;
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, " Datos erroneos");
                        txt_usuario.setText("");
                        txt_contrasenia.setText("");
                        return false;

                    }
                }
            }
        } catch (Exception e) {
            return false;
        }
        return false;
    }

